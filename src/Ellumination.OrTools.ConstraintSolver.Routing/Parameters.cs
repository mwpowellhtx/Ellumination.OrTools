    /// Represents Parameters that can be used for configuring either the Model or its Search.
    /// </summary>
    public abstract class Parameters
    {
        /// <summary>
        /// Constructs an instance of the <see cref="Parameters"/>.
        /// </summary>
        protected Parameters()
        {
        }

        /// <summary>
        /// Constructs an instance of the <see cref="Parameters"/>.
        /// </summary>
        /// <param name="_"></param>
        protected Parameters(object _)
        {
        }
    }
}
